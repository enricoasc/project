SELECT 
D1_FILIAL  FILIAL,
D1_DTDIGIT DATAENTRADA,
D1_COD PRODUTO,
D1_QUANT QUANTIDADE,
D1_VUNIT PRECO,
D1_TOTAL TOTAL,
F1_COND+'-'+E4_DESCRI CONDPAG, 
F1_FORNECE CODFORNECEDOR,
F1_LOJA LOJAFORNECEDOR, 
A2_NOME NOMEFORNECEDOR
FROM 
SD1010 SD1
INNER JOIN SF1010 SF1 ON F1_FILIAL = D1_FILIAL AND F1_DOC= D1_DOC AND F1_FORNECE = D1_FORNECE AND F1_LOJA = D1_LOJA 
INNER JOIN SA2010 SA2 ON D1_FORNECE = A2_COD AND D1_LOJA = A2_LOJA
INNER JOIN SE4010 SE4 ON F1_COND = E4_CODIGO
WHERE SD1.D_E_L_E_T_ =''
AND SF1.D_E_L_E_T_ =''
AND SA2.D_E_L_E_T_ = ''
AND SE4.D_E_L_E_T_ = ''
AND F1_DTDIGIT BETWEEN '20180101' AND '20181231'
AND D1_COD IN ('0301', '0303')
AND F1_TIPO = 'N'
AND D1_CF IN ('1652','2652')
ORDER BY FILIAL, DATAENTRADA, PRODUTO