--QUERY DE TODAS AS VENDAS DA DISTRIBUIDORA (COF) DE 5 ANOS 
-------------------
SELECT D2_FILIAL, D2_CF, D2_EMISSAO, D2_DOC, D2_SERIE, B1_COD, B1.B1_DESC, D2_QUANT, D2_PRCVEN,D2_TOTAL,D2_CLIENTE, D2_LOJA,A1_NOME , A1_EST
FROM SD2010 D2
INNER JOIN SB1010 B1 ON B1_COD = D2_COD
INNER JOIN SA1010 A1 ON A1_COD = D2_CLIENTE AND A1_LOJA = D2_LOJA
WHERE D2.D_E_L_E_T_ <> '*'
AND D2_EMISSAO BETWEEN '20170425' AND '20170630'
AND D2_CF IN ('5656','6556')
AND B1_GRUPO = '0001'
AND B1_COD NOT IN ('0201','0202','0301')
--AND D2_FILIAL NOT IN ('06','05','03')
--AND D2_FILIAL IN ('03')
AND D2_FILIAL NOT IN ('06','05','04','02','01')
ORDER BY D2_FILIAL, D2.D2_EMISSAO
