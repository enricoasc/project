-- GERA SENHA PARA RHONLINE 

SELECT RA.RA_MAT, RA.RA_NOME ,
		(SUBSTRING(RA.RA_NASC , 3, 2)+ 
		 SUBSTRING(RA.RA_ADMISSA , 7, 2)+ 
		 SUBSTRING(RA.RA_CIC , 10, 2)  ) AS SENHA
FROM SRA010 RA 
WHERE  RA.RA_DEMISSA ='' 
ORDER BY RA.RA_NOME

