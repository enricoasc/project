
	SELECT C7.C7_FILIAL,C7.C7_ITEM,B1.B1_DESC,C7.C7_PRECO,C7.C7_QUANT,
		   C7.C7_TOTAL,C7.C7_VLDESC,C7.C7_NUM,A2.A2_NOME,C7.C7_EMISSAO,C7.C7_OBS, D1.D1_DOC AS NFE
	FROM SC7020 C7 

	INNER JOIN SA2010 A2 ON C7.C7_FORNECE = A2.A2_COD AND C7.C7_LOJA = A2.A2_LOJA 
    INNER JOIN SB1010 B1 ON C7.C7_PRODUTO = B1.B1_COD 
    INNER JOIN SD1020 D1 ON C7.C7_FILIAL =  D1.D1_FILIAL AND C7.C7_NUM = D1.D1_PEDIDO 
						AND C7.C7_FORNECE = D1.D1_FORNECE AND C7.C7_LOJA = D1.D1_LOJA
						AND C7.C7_PRODUTO = D1.D1_COD AND C7.C7_ITEM = D1.D1_ITEMPC 

	WHERE C7.C7_FILIAL = '03'
	AND  C7.C7_CC = '06.04.014'
	AND  C7.D_E_L_E_T_ != '*'
	AND  A2.D_E_L_E_T_ != '*'
	AND  B1.D_E_L_E_T_ != '*'
	AND  D1.D_E_L_E_T_ != '*'
	
	
	UNION ALL
	
	SELECT C7.C7_FILIAL,C7.C7_ITEM,B1.B1_DESC,C7.C7_PRECO,C7.C7_QUANT,
		   C7.C7_TOTAL,C7.C7_VLDESC,C7.C7_NUM,A2.A2_NOME,C7.C7_EMISSAO,C7.C7_OBS, '' AS NFE
	FROM SC7020 C7 

	INNER JOIN SA2010 A2 ON C7.C7_FORNECE = A2.A2_COD AND C7.C7_LOJA = A2.A2_LOJA 
    INNER JOIN SB1010 B1 ON C7.C7_PRODUTO = B1.B1_COD 
 

	WHERE C7.C7_FILIAL = '03'
	AND  C7.C7_CC = '06.04.014'
	AND  C7.C7_NUM NOT IN (SELECT D1.D1_PEDIDO FROM SD1020 D1 WHERE D1.D1_FILIAL = '03' 
																	AND D1.D1_CC = '06.04.014'
																	AND D1.D_E_L_E_T_ != '*' )
	AND  C7.D_E_L_E_T_ != '*'
	AND  A2.D_E_L_E_T_ != '*'
	AND  B1.D_E_L_E_T_ != '*'
	ORDER BY C7.C7_EMISSAO, C7.C7_NUM, C7.C7_ITEM