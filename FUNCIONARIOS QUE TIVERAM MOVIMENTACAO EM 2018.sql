DECLARE @DATAANOINI DATE 
SET @DATAANOINI = '20180101'
DECLARE @DATAANOFIM DATE 
SET @DATAANOFIM = '20181231'

SELECT RA_NOME,RA_CIC, FORMAT(CAST(RA_ADMISSA AS date),'dd/MM/yyyy') AS 'ADMISSAO'  
FROM SRA010 WHERE RA_DEMISSA = '' 
AND RA_ADMISSA <= @DATAANOFIM 
AND RA_CIC NOT IN ('36191590687','61189871653','00456284672') /*REMOVE O FABIO, BRANQUINHO E ABREU*/

UNION 

SELECT RA_NOME,RA_CIC, FORMAT(CAST(RA_ADMISSA AS date),'dd/MM/yyyy') AS 'ADMISSAO'   FROM SRA050 WHERE RA_DEMISSA BETWEEN @DATAANOINI AND @DATAANOFIM 
ORDER BY RA_NOME



SELECT RA_FILIAL,RA_NOME,RA_CIC FROM SRA010 WHERE RA_CIC NOT IN ('36191590687','61189871653','00456284672')
AND RA_DEMISSA = ''
ORDER BY RA_FILIAL,RA_NOME


